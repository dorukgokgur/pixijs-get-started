let createApp = async () => {
  //Pixi App Açılışı
  let app = new PIXI.Application({ width: window.innerWidth, height: window.innerHeight, backgroundColor: "black" });

  //Body'e Pixi App'ini ekleme
  document.body.appendChild(app.view);

  //Karakter objesinin oluşturulması
  const spritesheet = new PIXI.Spritesheet(PIXI.BaseTexture.from(character.meta.image), character);

  //Karakter objesinin parse edilmesi
  await spritesheet.parse();

  //Container oluşturma
  const runContainer = new PIXI.Container();
  const idleContainer = new PIXI.Container();
  const attackContainer = new PIXI.Container();

  //Animasyonlar
  const animIdle = new PIXI.AnimatedSprite(spritesheet.animations.idle);
  const animAttack = new PIXI.AnimatedSprite(spritesheet.animations.attack);
  const animRun = new PIXI.AnimatedSprite(spritesheet.animations.run);

  //Animasyon hızları
  animAttack.animationSpeed = 0.1666;
  animIdle.animationSpeed = 0.1666;
  animRun.animationSpeed = 0.1666;

  //Animasyonların oynatılması
  animAttack.play();
  animIdle.play();
  animRun.play();

  //Animasyonların containerlara eklenmesi
  runContainer.addChild(animRun);
  idleContainer.addChild(animIdle);
  attackContainer.addChild(animAttack);

  //Containerların pozisyonları
  runContainer.x = 50;
  attackContainer.y = 50;

  //Containerların alpha değerleri
  attackContainer.alpha = 0.5;

  //Containerların ekrana eklenmesi
  app.stage.addChild(idleContainer);
  app.stage.addChild(attackContainer);
  app.stage.addChild(runContainer);
};

//Pixi App'ini oluşturmak için DOMContentLoaded eventi
document.addEventListener("DOMContentLoaded", createApp);
